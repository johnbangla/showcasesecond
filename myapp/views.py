def process_payment(request):
    order_id = create_ref_code()  # an order id created by our own function
    order = Order.objects.get(user=request.user, ordered=False) #User order object
    total = order.get_total()  #Total amount to be pain by paypal
    host = request.get_host() # user name to be recorded during transaction
    paypal_dict = {
        'business': settings.PAYPAL_RECEIVER_EMAIL,  'amount': total, 'item_name': 'Order {}'.format(order.id), 'invoice': str(order.id), 'currency_code': 'USD', 'notify_url': 'http://{}{}'.format(host, reverse('paypal-ipn')), 'return_url': 'http://{}{}'.format(host, reverse('payment_done')), 'cancel_return': 'http://{}{}'.format(host, reverse('payment_cancelled')), }
    form = PayPalPaymentsForm(initial=paypal_dict)
    return render(request, 'process_payment.html', {'order': order, 'form': form})


